// Integer
def myInt = 42
println "Integer: ${myInt}"

// Float
def myFloat = 3.14f
println "Float: ${myFloat}"

// Double
def myDouble = 3.14159
println "Double: ${myDouble}"

// Char (represented as a string of length 1 in Groovy)
def myChar = 'A'
println "Char: ${myChar}"

// String
def myString = "Hello, Groovy!"
println "String: ${myString}"

// Boolean
def myBoolean = true
println "Boolean: ${myBoolean}"

// Array
def myArray = [1, 2, 3, 4, 5] as int[]
println "Array: ${myArray.join(', ')}"

// List
def myList = [1, 2, 3, 4, 5]
println "List: ${myList.join(', ')}"

